package api.stonedt.com.entity;

import java.util.Date;

public class NewsEntity {
	private Integer id;
	private String identification;//评论用户名
	private String content;//原始html页面
	private String text;//文章纯文本
	private String weburl;//原始url
	private Date date;//日期
	private String title;//标题
	private String keyword;//关键词
	private Integer hospitalid;//医院id
	private Integer emotion;//1代表正面、2代表负面、3代表中性
	private Integer kind;//类别1代表党性工作主题、2代表反腐倡廉主题、3代表医疗服务主题、4代表医学研究主题、5代表相关舆情
	private String originweb;//原始网站
	private String abstractcontent;//概要
	private Integer otherhospitalid;//其他医院id
	private Integer comment;//1代表新闻、2代表用户评论
	private Integer doctorid;//相关医生的id
	private Integer type;//医生相关新闻的类型，1代表网络相关、2代表学术著作
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getIdentification() {
		return identification;
	}
	public void setIdentification(String identification) {
		this.identification = identification;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getWeburl() {
		return weburl;
	}
	public void setWeburl(String weburl) {
		this.weburl = weburl;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public Integer getHospitalid() {
		return hospitalid;
	}
	public void setHospitalid(Integer hospitalid) {
		this.hospitalid = hospitalid;
	}
	public Integer getEmotion() {
		return emotion;
	}
	public void setEmotion(Integer emotion) {
		this.emotion = emotion;
	}
	public Integer getKind() {
		return kind;
	}
	public void setKind(Integer kind) {
		this.kind = kind;
	}
	public String getOriginweb() {
		return originweb;
	}
	public void setOriginweb(String originweb) {
		this.originweb = originweb;
	}
	public String getAbstractcontent() {
		return abstractcontent;
	}
	public void setAbstractcontent(String abstractcontent) {
		this.abstractcontent = abstractcontent;
	}
	public Integer getOtherhospitalid() {
		return otherhospitalid;
	}
	public void setOtherhospitalid(Integer otherhospitalid) {
		this.otherhospitalid = otherhospitalid;
	}
	public Integer getComment() {
		return comment;
	}
	public void setComment(Integer comment) {
		this.comment = comment;
	}
	public Integer getDoctorid() {
		return doctorid;
	}
	public void setDoctorid(Integer doctorid) {
		this.doctorid = doctorid;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	
}
